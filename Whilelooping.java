public class Whilelooping {
    public static void main(String[] args) {

        int i = 2; //deklarasi dan inisialisasi
        while (i <= 5) {
            System.out.println("Darling adek ka jill");
            i = i + 1; // control loop
        }
        System.out.println("10 Gelombang kanan");

    }
}
